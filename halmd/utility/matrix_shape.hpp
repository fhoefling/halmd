#ifndef HALMD_UTILITY_MATRIX_SHAPE_HPP
#define HALMD_UTILITY_MATRIX_SHAPE_HPP

namespace halmd {

template <typename T, typename S>
static T const&
check_shape(T const& m1, S const& m2)
{
    if (m1.size1() != m2.size1() || m1.size2() != m2.size2()) {
        throw std::invalid_argument("parameter matrix has invalid shape");
    }
    return m1;
}

} // namespace halmd

#endif /* ! HALMD_UTILITY_MATRIX_SHAPE_HPP */
